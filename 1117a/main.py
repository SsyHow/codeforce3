import sys

input = lambda: sys.stdin.readline().rstrip()


def I():
    return input()


def II():
    return int(input())


def MII():
    return map(int, input().split())


def LI():
    return list(input().split())


def LII():
    return list(map(int, input().split()))

# alpha = ['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h', 'i', 'j', 'k', 'l', 'm', 'n', 'o', 'p', 'q', 'r', 's', 't', 'u', 'v', 'w', 'x', 'y', 'z']
a = II()
L = LII()

tmp = 1
ans = 1
prev = L[0]
x = max(L)
for i in range(1, a):
    if L[i] == prev == x:
        tmp += 1
        ans = max(tmp, ans)
    else:
        tmp = 1
        prev = L[i]
print(ans)